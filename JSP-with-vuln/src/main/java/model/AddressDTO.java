package model;

public class AddressDTO {
    private String sido;
    private String sigungu;
    private String doro_name;
    private String sigungu_building_name;

    public String getSido() {
        return sido;
    }

    public void setSido(String sido) {
        this.sido = sido;
    }

    public String getSigungu() {
        return sigungu;
    }

    public void setSigungu(String sigungu) {
        this.sigungu = sigungu;
    }

    public String getDoro_name() {
        return doro_name;
    }

    public void setDoro_name(String doro_name) {
        this.doro_name = doro_name;
    }

    public String getSigungu_building_name() {
        return sigungu_building_name;
    }

    public void setSigungu_building_name(String sigungu_building_name) {
        this.sigungu_building_name = sigungu_building_name;
    }
}
